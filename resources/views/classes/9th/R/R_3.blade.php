                Main article: Mammalian reproduction
                <br><br>
                See also: Human reproduction
                There are three extant kinds of mammals: monotremes, placentals and marsupials, all with internal fertilization. In placental mammals, offspring are born as juveniles: complete animals with the sex organs present although not reproductively functional. After several months or years, depending on the species, the sex organs develop further to maturity and the animal becomes sexually mature. Most female mammals are only fertile during certain periods during their estrous cycle, at which point they are ready to mate. Individual male and female mammals meet and carry out copulation.[citation needed] For most mammals, males and females exchange sexual partners throughout their adult lives.[28][29][30]
