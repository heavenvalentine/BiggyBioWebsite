            <h4 class="subtitle-note mt-3 mb-2">Evolution
            </h4>
            <p class="desc-note mt-2">
                Main article: Evolution of sexual reproduction
                <br><br>
                The first fossilized evidence of sexual reproduction in eukaryotes is from the Stenian period, about 1.05 billion years old.[18][19]
                <br><br>
                Biologists studying evolution propose several explanations for the development of sexual reproduction and its maintenance. These reasons include reducing the likelihood of the accumulation of deleterious mutations, increasing rate of adaptation to changing environments,[20] dealing with competition, DNA repair, masking deleterious mutations, and reducing genetic variation on the genomic level.[21][22][23][24] All of these ideas about why sexual reproduction has been maintained are generally supported, but ultimately the size of the population determines if sexual reproduction is entirely beneficial. Larger populations appear to respond more quickly to some of the benefits obtained through sexual reproduction than do smaller population sizes.[25]
                <br><br>
                Maintenance of sexual reproduction has been explained by theories that work at several levels of selection, though some of these models remain controversial.[citation needed] However, newer models presented in recent years suggest a basic advantage for sexual reproduction in slowly reproducing complex organisms.
                <br><br>
                Sexual reproduction allows these species to exhibit characteristics that depend on the specific environment that they inhabit, and the particular survival strategies that they employ.[26]
	        </p>

            <h4 class="subtitle-note mt-3 mb-2">Sexual selection
            </h4>
            <p class="desc-note mt-2">
                Main article: Sexual selection
                <br><br>
                In order to reproduce sexually, both males and females need to find a mate. Generally in animals mate choice is made by females while males compete to be chosen. This can lead organisms to extreme efforts in order to reproduce, such as combat and display, or produce extreme features caused by a positive feedback known as a Fisherian runaway. Thus sexual reproduction, as a form of natural selection, has an effect on evolution. Sexual dimorphism is where the basic phenotypic traits vary between males and females of the same species. Dimorphism is found in both sex organs and in secondary sex characteristics, body size, physical strength and morphology, biological ornamentation, behavior and other bodily traits. However, sexual selection is only implied over an extended period of time leading to sexual dimorphism.[27]
	        </p>

