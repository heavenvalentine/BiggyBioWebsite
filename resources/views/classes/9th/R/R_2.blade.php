            <h4 class="subtitle-note mt-3 mb-2">Fragmentation
            </h4>
            <p class="desc-note mt-2">
                Regeneration from an arm
                Linckia guildingi "comet", a starfish regrowing from a single arm
                Main article: Fragmentation (reproduction)
                Fragmentation is a form of asexual reproduction where a new organism grows from a fragment of the parent. Each fragment develops into a mature, fully grown individual. Fragmentation is seen in many organisms. Animals that reproduce asexually include planarians, many annelid worms including polychaetes[17] and some oligochaetes,[18] turbellarians and sea stars. Many fungi and plants reproduce asexually. Some plants have specialized structures for reproduction via fragmentation, such as gemmae in liverworts. Most lichens, which are a symbiotic union of a fungus and photosynthetic algae or cyanobacteria, reproduce through fragmentation to ensure that new individuals contain both symbionts. These fragments can take the form of soredia, dust-like particles consisting of fungal hyphen wrapped around photobiont cells.

                <div class="desc-note mt-2 text-center">
                    <img src="asset/img/fragment.jpg" class="w-75" " alt="" srcset="">
                </div>

                Clonal Fragmentation in multicellular or colonial organisms is a form of asexual reproduction or cloning where an organism is split into fragments. Each of these fragments develop into mature, fully grown individuals that are clones of the original organism. In echinoderms, this method of reproduction is usually known as fissiparity.[19] Due to many environmental and epigenetic differences, clones originating from the same ancestor might actually be genetically and epigenetically different.[20]
	        </p>

            <h4 class="subtitle-note mt-3 mb-2">Spore formation
            </h4>
            <p class="desc-note mt-2">
                Main article: Sporogenesis
                Many multicellular organisms form spores during their biological life cycle in a process called sporogenesis. Exceptions are animals and some protists, which undergo meiosis immediately followed by fertilization. Plants and many algae on the other hand undergo sporic meiosis where meiosis leads to the formation of haploid spores rather than gametes. These spores grow into multicellular individuals (called gametophytes in the case of plants) without a fertilization event. These haploid individuals give rise to gametes through mitosis. Meiosis and gamete formation therefore occur in separate generations or "phases" of the life cycle, referred to as alternation of generations. Since sexual reproduction is often more narrowly defined as the fusion of gametes (fertilization), spore formation in plant sporophytes and algae might be considered a form of asexual reproduction (agamogenesis) despite being the result of meiosis and undergoing a reduction in ploidy. However, both events (spore formation and fertilization) are necessary to complete sexual reproduction in the plant life cycle.
                <br><br>
                Fungi and some algae can also utilize true asexual spore formation, which involves mitosis giving rise to reproductive cells called mitospores that develop into a new organism after dispersal. This method of reproduction is found for example in conidial fungi and the red algae Polysiphonia, and involves sporogenesis without meiosis. Thus the chromosome number of the spore cell is the same as that of the parent producing the spores. However, mitotic sporogenesis is an exception and most spores, such as those of plants and many algae, are produced by meiosis.[citation needed][14][15][16]
