            <h4 class="subtitle-note mt-3 mb-2">Vegetative propagation
            </h4>
            <p class="desc-note mt-2">
                Main article: Vegetative propagation

                <div class="desc-note mt-2 text-center">
                    <img src="asset/img/vegetative.jpg" class="w-75" " alt="" srcset="">
                </div>

                Vegetative plantlets of mother-of-thousands, Bryophyllum daigremontianum (Kalanchoe daigremontiana)
                Vegetative propagation is a type of asexual reproduction found in plants where new individuals are formed without the production of seeds or spores and thus without syngamy or meiosis.[12] Examples of vegetative reproduction include the formation of miniaturized plants called plantlets on specialized leaves, for example in kalanchoe (Bryophyllum daigremontianum) and many produce new plants from rhizomes or stolon (for example in strawberry). Other plants reproduce by forming bulbs or tubers (for example tulip bulbs and Dahlia tubers). Some plants produce adventitious shoots and may form a clonal colony. In these examples, all the individuals are clones, and the clonal population may cover a large area.[13]
