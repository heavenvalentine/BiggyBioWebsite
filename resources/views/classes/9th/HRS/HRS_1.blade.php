            <h4 class="subtitle-note mt-3 mb-2">What are the internal male reproductive organs?
            </h4>
            <p class="desc-note mt-2">
                You have several internal organs — also called accessory organs — that play a big part in the male reproductive system. These organs include:
                <ol>
                <li>Vas deferens: The vas deferens is a long, muscular tube that travels from the epididymis into the pelvic cavity, to just behind the bladder. The vas deferens transports mature sperm to the urethra in preparation for ejaculation.</li>

                <li>Ejaculatory ducts: These ducts are formed by the fusion of the vas deferens and the seminal vesicles. The ejaculatory ducts empty into the urethra.</li>

                <li>Urethra: The urethra is the tube that carries urine from the bladder to outside of your body. In males, it has the additional function of expelling (ejaculating) semen when you reach orgasm. When the penis is erect during sex, the flow of urine is blocked from the urethra, allowing only semen to be ejaculated at orgasm.</li>

                <li>Seminal vesicles: The seminal vesicles are sac-like pouches that attach to the vas deferens near the base of the bladder. The seminal vesicles make a sugar-rich fluid (fructose) that provides sperm with a source of energy and helps with the sperms’ ability to move (motility). The fluid of the seminal vesicles makes up most of the volume of your ejaculatory fluid, or ejaculate.</li>

                <li>Prostate gland: The prostate gland is a walnut-sized structure that’s located below the urinary bladder in front of the rectum. The prostate gland contributes additional fluid to the ejaculate. Prostate fluids also help to nourish the sperm. The urethra, which carries the ejaculate to be expelled during orgasm, runs through the center of the prostate gland.
                Bulbourethral glands: The bulbourethral glands, or Cowper’s glands, are pea-sized structures located on the sides of the urethra, just below the prostate gland. These glands produce a clear, slippery fluid that empties directly into the urethra. This fluid serves to lubricate the urethra and to neutralize any acidity that may be present due to residual drops of urine in the urethra.</li>
                </ol>
