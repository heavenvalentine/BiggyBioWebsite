            <h4 class="subtitle-note mt-3 mb-2">What are the external male reproductive structures?
            </h4>
            <p class="desc-note mt-2">
                Most of the male reproductive system is located outside of your abdominal cavity or pelvis. The external parts of the male reproductive system include the penis, the scrotum and the testicles.
                <ol>
                <li>Penis
                The penis is the male organ for sexual intercourse. It has three parts:</li>

                <li>The root: This is the part of the penis that attaches to the wall of your abdomen.
                The body or shaft: Shaped like a tube or cylinder, the body of the penis is made up of three internal chambers. Inside these chambers there’s a special, sponge-like erectile tissue that contains thousands of large spaces that fill with blood when you’re sexually aroused. As the penis fills with blood, it becomes rigid and erect, which allows for penetration during sex. The skin of the penis is loose and elastic, allowing for changes in penis size during an erection.
                The glans: This is the cone-shaped end of the penis. The glans, which is also called the head of the penis, is covered with a loose layer of skin called foreskin. This skin is sometimes removed in a procedure called circumcision.
                The opening of the urethra — the tube that transports both semen and urine out of the body — is located at the tip of the glans penis. The penis also contains many sensitive nerve endings.
                Semen, which contains sperm, is expelled (ejaculated) through the end of the penis when a man reaches sexual climax (orgasm). When the penis is erect, the flow of urine is blocked from the urethra, allowing only semen to be ejaculated at orgasm.</li>

                <li>Scrotum

                The scrotum is the loose pouch-like sac of skin that hangs behind the penis. It holds the testicles (also called testes), as well as many nerves and blood vessels. The scrotum protects your testes, as well as providing a sort of climate control system. For normal sperm development, the testes must be at a temperature slightly cooler than the body temperature. Special muscles in the wall of the scrotum allow it to contract (tighten) and relax, moving the testicles closer to the body for warmth and protection or farther away from the body to cool the temperature.</li>

                <li>Testicles (testes)

                The testes are oval organs about the size of very large olives that lie in the scrotum, secured at either end by a structure called the spermatic cord. Most men have two testes. The testes are responsible for making testosterone, the primary male sex hormone, and for producing sperm. Within the testes are coiled masses of tubes called seminiferous tubules. These tubules are responsible for producing the sperm cells through a process called spermatogenesis.</li>

                <li>Epididymis

                The epididymis is a long, coiled tube that rests on the backside of each testicle. It carries and stores sperm cells that are created in the testes. It’s also the job of the epididymis to bring the sperm to maturity — the sperm that emerge from the testes are immature and incapable of fertilization. During sexual arousal, contractions force the sperm into the vas deferens.</li>
                </ol>
