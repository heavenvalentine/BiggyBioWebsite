            <h4 class="subtitle-note mt-3 mb-2">Examples of Anabolic Processes
            </h4>

            <h4 class="subtitle-note mt-3 mb-2">Protein Synthesis
            </h4>
            <p class="desc-note mt-2">

                Proteins are macromolecules that carry out cellular activities encoded by an organism’s genes. They have many different functions in the body, including DNA replication, aiding chemical reactions (as enzymes), transporting materials in the cell, cell growth and signaling, and providing physical structure. Each cell in the human body contains about 1 to 3 billion proteins.
                <br><br>
                Proteins are synthesized from smaller molecules called amino acids in a cell’s ribosomes. Each protein is a chain of a specific sequence of amino acids. Since proteins are larger molecules put together from smaller ones, the process of protein synthesis is anabolic.
	        </p>

            <h4 class="subtitle-note mt-3 mb-2"> DNA Synthesis
            </h4>
            <p class="desc-note mt-2">

                Deoxyribonucleic acid, or DNA, is an organism’s genetic material. It is macromolecule made up of smaller molecules called nucleic acids, which are themselves made up of a nucleotide base attached to a deoxyribose sugar and a phosphate molecule. DNA synthesis is an anabolic process that takes place in a cell’s nucleus just before the cell divides. It involves unzipping the double strands of DNA and attaching new matching nucleotides to each half of the strand that has been unzipped, forming two new strands that each contain half of the old DNA strand.
	        </p>

            <h4 class="subtitle-note mt-3 mb-2"> Growth of Bones and Muscles
            </h4>
            <p class="desc-note mt-2">

                On a larger scale, the growth of body parts such as bones and muscles is anabolic. Bone growth, or ossification, occurs when bone is formed from cells called osteoclasts. It is then mineralized through cells called osteoblasts. This process is also anabolic; during mineralization, osteoblasts produce calcium phosphate crystals that are incorporated into the bone’s structure, making the bones hard and sturdy.
                <br><br>
                Muscle growth, also called muscle hypertrophy, occurs when the cells of skeletal muscles, called myocytes, increase in size. It occurs through strength training exercises such as lifting weights. Factors like sex, age, and diet all affect hypertrophy. During hypertrophy, there is increased protein synthesis of actin and myosin, and the volume of sarcoplasmic fluid in the myocyte increases.
	        </p>

