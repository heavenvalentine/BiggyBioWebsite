            <h4 class="subtitle-note mt-3 mb-2">Structure
            </h4>
            <p class="desc-note mt-2">
                There are three types of muscle tissue in vertebrates: skeletal, cardiac, and smooth. Skeletal and cardiac muscle are types of striated muscle tissue.[2] Smooth muscle is non-striated.
                <br><br>
                Skeletal muscle tissue is an elongated striated muscle tissue ranging from several millimeters to about 10 centimeters in length and from 10 to 100 micrometers in width.[3] Skeletal striated muscle tissue is arranged in regular, parallel bundles of myofibrils containing the many contractile units known as sarcomeres, which give the tissue its striated (striped) appearance. Skeletal muscle, is voluntary muscle anchored by tendons or sometimes by aponeuroses to bones, and is used to effect skeletal movement such as locomotion and to maintain posture. Postural control is generally maintained as an unconscious reflex, but the muscles responsible can also react to conscious control. An average adult man is made up of 42% of skeletal muscle as a percentage of body mass, and an average adult woman is made up of 36%.[4]
                <br><br>
                Cardiac muscle tissue, is found only in the walls of the heart as myocardium, and is involuntary being controlled by the autonomic nervous system. Cardiac muscle tissue is striated like skeletal muscle, containing contractile units called sarcomeres in highly regular arrangements of bundles. While skeletal muscles are arranged in regular, parallel bundles, cardiac muscle connects at branching, irregular angles known as intercalated discs.
                <br><br>
                Smooth muscle tissue is non-striated and involuntary. Smooth muscle is found within the walls of organs and structures such as the esophagus, stomach, intestines, bronchi, uterus, urethra, bladder, blood vessels, and the arrector pili in the skin which controls the erection of body hair.
	        </p>

            <h4 class="subtitle-note mt-3 mb-2">Cardiac muscle
            </h4>
            <p class="desc-note mt-2">
                Cardiac muscle is involuntary, striated muscle that is found in the walls and histological foundation of the heart, specifically the myocardium. The cardiac muscle cells, (also called cardiomyocytes or myocardiocytes), predominantly contain only one nucleus, although populations with two to four nuclei do exist.[10][11][page needed] The myocardium is the muscle tissue of the heart and forms a thick middle layer between the outer epicardium layer and the inner endocardium layer.
                <br><br>
                Coordinated contractions of cardiac muscle cells in the heart propel blood out of the atria and ventricles to the blood vessels of the left/body/systemic and right/lungs/pulmonary circulatory systems. This complex mechanism illustrates systole of the heart.
                <br><br>
                Cardiac muscle cells, unlike most other tissues in the body, rely on an available blood and electrical supply to deliver oxygen and nutrients and remove waste products such as carbon dioxide. The coronary arteries help fulfill this function.
